        private async Task<Dictionary<string, Exercise>> EnsureSeedExercisesAsync(
            string userId,
            CancellationToken ct)
        {
            var specs = ExerciseSpecs();
            var names = specs.Select(x => x.Name).ToList();
            var existing = await _db.Exercises
                .Where(x => x.UserId == userId && names.Contains(x.Name))
                .ToListAsync(ct);
            var byName = existing
                .GroupBy(x => x.Name, StringComparer.OrdinalIgnoreCase)
                .ToDictionary(x => x.Key, x => x.First(), StringComparer.OrdinalIgnoreCase);
            var existingExerciseIds = existing.Select(x => x.Id).ToList();

            if (existingExerciseIds.Count > 0)
            {
                await _db.ExerciseMuscles
                    .Where(x => existingExerciseIds.Contains(x.ExerciseId))
                    .ExecuteDeleteAsync(ct);
            }

            foreach (var spec in specs)
            {
                if (!byName.TryGetValue(spec.Name, out var exercise))
                {
                    exercise = new Exercise
                    {
                        Name = spec.Name,
                        Description = SeedNote,
                        Muscle = spec.Muscle,
                        SpecificMuscleGroups = string.Join(", ", spec.Muscles.Select(x => x.Muscle)),
                        Equipment = spec.Equipment,
                        Category = "Strength",
                        EquipmentType = spec.Equipment,
                        IsBodyweight = spec.IsBodyweight,
                        IsIsolation = spec.IsIsolation,
                        IsCompound = !spec.IsIsolation,
                        DefaultProgressionStepKg = spec.ProgressionStepKg,
                        UserId = userId
                    };

                    exercise.ExerciseMuscles = BuildExerciseMuscles(exercise.Id, spec.Muscles);
                    _db.Exercises.Add(exercise);
                    byName[spec.Name] = exercise;
                    continue;
                }

                exercise.Description = SeedNote;
                exercise.Muscle = spec.Muscle;
                exercise.SpecificMuscleGroups = string.Join(", ", spec.Muscles.Select(x => x.Muscle));
                exercise.Equipment = spec.Equipment;
                exercise.Category = "Strength";
                exercise.EquipmentType = spec.Equipment;
                exercise.IsBodyweight = spec.IsBodyweight;
                exercise.IsIsolation = spec.IsIsolation;
                exercise.IsCompound = !spec.IsIsolation;
                exercise.DefaultProgressionStepKg = spec.ProgressionStepKg;

                var replacementMuscles = BuildExerciseMuscles(exercise.Id, spec.Muscles);
                _db.ExerciseMuscles.AddRange(replacementMuscles);
                exercise.ExerciseMuscles = replacementMuscles;
            }

            await _db.SaveChangesAsync(ct);
            return byName;
        }

        private static List<ExerciseMuscle> BuildExerciseMuscles(
