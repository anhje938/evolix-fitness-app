                {
                    ExerciseId = exerciseId,
                    Muscle = x.Muscle,
                    Role = x.Role,
                    Contribution = x.Contribution
                })
                .ToList();
        }

        private async Task<SeedWorkoutProgram> EnsureSeedWorkoutProgramAsync(
            string userId,
            IReadOnlyDictionary<string, Exercise> exercises,
            CancellationToken ct)
        {
            var program = await _db.WorkoutPrograms
                .FirstOrDefaultAsync(
                    x => x.UserId == userId && x.Name == SeedProgramName,
                    ct);

            if (program == null)
            {
                program = new WorkoutProgram
                {
                    Id = Guid.NewGuid(),
                    Name = SeedProgramName,
                    Goal = "Muskelvekst og styrke med EvoliX-planlegging",
                    Level = "Viderekommen",
                    UserId = userId
                };
                _db.WorkoutPrograms.Add(program);
            }
            else
            {
                program.Goal = "Muskelvekst og styrke med EvoliX-planlegging";
                program.Level = "Viderekommen";
            }

            var specs = SeedWorkoutSpecs();
            var workoutNames = specs.Select(x => x.Name).ToList();
            var existingWorkouts = await _db.Workouts
                .Include(x => x.WorkoutExercises)
                .Where(x => x.UserId == userId && workoutNames.Contains(x.Name))
                .ToListAsync(ct);
            var byName = existingWorkouts.ToDictionary(x => x.Name, StringComparer.OrdinalIgnoreCase);
            var bySplit = new Dictionary<SplitType, Workout>();

            foreach (var spec in specs)
            {
                if (!byName.TryGetValue(spec.Name, out var workout))
                {
                    workout = new Workout
                    {
                        Id = Guid.NewGuid(),
                        Name = spec.Name,
                        UserId = userId
                    };
                    _db.Workouts.Add(workout);
                }
                else if (workout.WorkoutExercises.Count > 0)
                {
                    _db.WorkoutExercises.RemoveRange(workout.WorkoutExercises);
                    workout.WorkoutExercises.Clear();
                }

                workout.Description = spec.Description;
                workout.DayLabel = spec.DayLabel;
                workout.WorkoutProgramId = program.Id;

                var exerciseIds = WorkoutPlans(spec.Split)
                    .Select(x => exercises[x.ExerciseName].Id)
                    .ToList();

                foreach (var workoutExercise in exerciseIds.Select((exerciseId, index) => new WorkoutExercise
                {
                    WorkoutId = workout.Id,
                    ExerciseId = exerciseId,
                    Order = index + 1
                }))
                {
                    workout.WorkoutExercises.Add(workoutExercise);
                }

                bySplit[spec.Split] = workout;
            }

            await _db.SaveChangesAsync(ct);
            return new SeedWorkoutProgram(program, bySplit);
        }

        private async Task<int> ReplaceExerciseTargetsAsync(
            string userId,
            IReadOnlyDictionary<string, Exercise> exercises,
            CancellationToken ct)
        {
            var seedExerciseIds = exercises.Values.Select(x => x.Id).ToList();
            var existing = await _db.ExerciseTargets
                .Where(x => x.UserId == userId && seedExerciseIds.Contains(x.ExerciseId))
                .ToListAsync(ct);

            if (existing.Count > 0)
            {
                _db.ExerciseTargets.RemoveRange(existing);
                await _db.SaveChangesAsync(ct);
            }

            var targets = ExerciseTargetSpecs()
                .Select(spec => new ExerciseTarget
                {
                    UserId = userId,
                    ExerciseId = exercises[spec.ExerciseName].Id,
                    TargetSets = spec.TargetSets,
                    MinReps = spec.MinReps,
                    MaxReps = spec.MaxReps,
                    TargetWeightKg = spec.TargetWeightKg,
                    ProgressionModel = spec.ProgressionModel,
                    UpdatedAtUtc = DateTime.UtcNow
                })
                .ToList();

            _db.ExerciseTargets.AddRange(targets);
            await _db.SaveChangesAsync(ct);
            return targets.Count;
        }

