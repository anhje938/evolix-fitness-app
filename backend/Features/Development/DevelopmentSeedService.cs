        private static int ScaleInt(int value, double factor)
        {
            return Math.Max(0, (int)Math.Round(value * factor, MidpointRounding.AwayFromZero));
        }

        private static double RoundToNearest(double value, double step)
        {
            return Math.Round(value / step, MidpointRounding.AwayFromZero) * step;
        }

