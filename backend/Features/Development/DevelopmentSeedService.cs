        private static List<FoodLog> BuildFoodLogs(
            string userId,
            DateTime fromDay,
            DateTime toDay)
        {
            var meals = new[]
            {
                new MealTemplate("Oatmeal bowl", 520, 36, 70, 14, 7),
                new MealTemplate("Chicken rice bowl", 690, 52, 82, 17, 12),
                new MealTemplate("Skyr, banana and almonds", 360, 32, 42, 8, 15),
                new MealTemplate("Salmon, potatoes and greens", 760, 48, 74, 28, 18)
            };

            var logs = new List<FoodLog>();
            var dayCount = (int)(toDay - fromDay).TotalDays + 1;

            for (var dayIndex = 0; dayIndex < dayCount; dayIndex++)
            {
                var day = fromDay.AddDays(dayIndex);
                var isTrainingDay = day.DayOfWeek != DayOfWeek.Sunday;
                var dailyAdjustment = isTrainingDay ? 1.04 : 0.94;
                var wave = Math.Sin(dayIndex * 0.75) * 0.04;

                for (var mealIndex = 0; mealIndex < meals.Length; mealIndex++)
                {
                    var meal = meals[mealIndex];
                    var factor = dailyAdjustment + wave + (mealIndex == 3 ? 0.03 : 0);
                    logs.Add(new FoodLog
                    {
                        UserId = userId,
                        Title = meal.Title,
                        Calories = ScaleInt(meal.Calories, factor),
                        Proteins = ScaleInt(meal.Proteins, factor),
                        Carbs = ScaleInt(meal.Carbs, factor),
                        Fats = ScaleInt(meal.Fats, factor),
                        TimestampUtc = day.AddHours(meal.HourUtc).AddMinutes((dayIndex % 4) * 7),
                        SourceType = SeedSourceType,
                        SourceServings = 1m
                    });
                }
            }

            return logs;
        }

        private async Task<int> UpsertWeightLogsAsync(
            string userId,
            DateTime fromDay,
            DateTime toDay,
            CancellationToken ct)
        {
            var existing = await _db.WeightLogs
                .Where(x =>
                    x.UserId == userId &&
                    x.TimestampUtc >= fromDay &&
                    x.TimestampUtc < toDay.AddDays(1))
                .ToListAsync(ct);

            var byDay = existing
                .GroupBy(x => x.TimestampUtc.Date)
                .ToDictionary(x => x.Key, x => x.First());

            var dayCount = (int)(toDay - fromDay).TotalDays + 1;

            for (var dayIndex = 0; dayIndex < dayCount; dayIndex++)
            {
                var day = fromDay.AddDays(dayIndex);
                var trend = 86.4 - (dayIndex * 0.035);
                var noise = Math.Sin(dayIndex * 1.7) * 0.18 + Math.Cos(dayIndex * 0.41) * 0.07;
                var weightKg = Math.Round(trend + noise, 1, MidpointRounding.AwayFromZero);
                var timestamp = day.AddHours(6).AddMinutes(20 + dayIndex % 11);

                if (byDay.TryGetValue(day, out var existingLog))
                {
                    existingLog.WeightKg = weightKg;
                    existingLog.TimestampUtc = timestamp;
                    continue;
                }

                _db.WeightLogs.Add(new WeightLog
                {
                    UserId = userId,
                    WeightKg = weightKg,
                    TimestampUtc = timestamp
                });
            }

            return dayCount;
        }

        private static List<WorkoutSession> BuildWorkoutSessions(
            string userId,
            DateTime trainingFrom,
            IReadOnlyDictionary<string, Exercise> exercises,
            SeedWorkoutProgram plan)
        {
            var sessions = new List<WorkoutSession>();
            var counts = new Dictionary<SplitType, int>
            {
                [SplitType.Push] = 0,
                [SplitType.Pull] = 0,
                [SplitType.Legs] = 0
            };

            for (var dayOffset = 0; counts.Values.Any(x => x < 10); dayOffset++)
            {
                var cycleDay = dayOffset % 7;
                if (cycleDay == 6) continue;

                var split = cycleDay switch
                {
                    0 or 3 => SplitType.Push,
                    1 or 4 => SplitType.Pull,
                    _ => SplitType.Legs
                };

                if (counts[split] >= 10) continue;

                var occurrence = counts[split];
                counts[split]++;
                var startedAt = trainingFrom
                    .AddDays(dayOffset)
                    .AddHours(16)
                    .AddMinutes(25 + (occurrence % 4) * 8);

                sessions.Add(BuildWorkoutSession(
                    userId,
                    split,
                    occurrence,
                    startedAt,
                    exercises,
                    plan));
            }

            return sessions;
        }

        private static WorkoutSession BuildWorkoutSession(
            string userId,
            SplitType split,
            int occurrence,
            DateTime startedAt,
            IReadOnlyDictionary<string, Exercise> exercises,
            SeedWorkoutProgram plan)
        {
            var plans = WorkoutPlans(split);
            var workout = plan.Workouts[split];
            var session = new WorkoutSession
            {
                UserId = userId,
                ClientRequestId = $"{SeedSessionPrefix}{split.ToString().ToLowerInvariant()}-{occurrence + 1:D2}",
                WorkoutId = workout.Id,
                WorkoutProgramId = plan.Program.Id,
                StartedAtUtc = startedAt,
                FinishedAtUtc = startedAt.AddMinutes(62 + (occurrence % 5) * 4),
                Title = $"{workout.DayLabel} #{occurrence + 1}",
                Notes = SeedNote
            };

            for (var exerciseIndex = 0; exerciseIndex < plans.Count; exerciseIndex++)
            {
                var exercisePlan = plans[exerciseIndex];
                var exerciseLog = new WorkoutExerciseLog
                {
                    ExerciseId = exercises[exercisePlan.ExerciseName].Id,
                    Order = exerciseIndex + 1
                };

                for (var setIndex = 0; setIndex < exercisePlan.Sets; setIndex++)
                {
                    var weight = RoundToNearest(
                        exercisePlan.StartWeightKg + occurrence * exercisePlan.ProgressionKg - setIndex * exercisePlan.BackoffKg,
                        2.5);
                    var reps = Math.Max(5, exercisePlan.TargetReps - ((occurrence + setIndex) % 2));

                    exerciseLog.Sets.Add(new SetLog
                    {
                        SetNumber = setIndex + 1,
                        WeightKg = weight,
                        Reps = reps,
                        Rir = Math.Round(1.5 + ((occurrence + setIndex) % 4) * 0.5, 1),
                        SetType = setIndex == 0 ? "top" : "backoff"
                    });
                }

                session.ExerciseLogs.Add(exerciseLog);
            }

            RecalculateTotals(session);
            return session;
        }

        private static void RecalculateTotals(WorkoutSession session)
        {
            var sets = session.ExerciseLogs.SelectMany(x => x.Sets).ToList();
            session.TotalSets = sets.Count;
            session.TotalReps = sets.Where(x => x.Reps.HasValue).Sum(x => x.Reps!.Value);
            session.TotalVolume = sets
                .Where(x => x.WeightKg.HasValue && x.Reps.HasValue)
                .Sum(x => x.WeightKg!.Value * x.Reps!.Value);
        }

        private static List<AdaptiveRecommendation> BuildDevelopmentRecommendations(
            string userId,
            DateTime today,
            WeeklyReport report,
            UserSettings settings,
            IReadOnlyDictionary<string, Exercise> exercises)
        {
            var createdAt = DateTime.UtcNow;
