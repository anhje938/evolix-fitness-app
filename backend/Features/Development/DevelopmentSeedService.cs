        public async Task<DevelopmentSeedResult> SeedMockDataAsync(
            string userId,
            CancellationToken ct = default)
        {
            if (string.IsNullOrWhiteSpace(userId))
                throw new ArgumentException("UserId is required.", nameof(userId));

            var today = DateTime.UtcNow.Date;
            var foodAndWeightFrom = today.AddDays(-29);
            var trainingFrom = today.AddDays(-34);

            await RemoveExistingSeedDataAsync(userId, ct);
            var settings = await UpsertUserSettingsAsync(userId, today, ct);
            await UpsertCoachSettingsAsync(userId, ct);
            var exercises = await EnsureSeedExercisesAsync(userId, ct);
            var plan = await EnsureSeedWorkoutProgramAsync(userId, exercises, ct);
            var exerciseTargets = await ReplaceExerciseTargetsAsync(userId, exercises, ct);
            var nutritionTargetsHistory = await ReplaceNutritionTargetsHistoryAsync(
                userId,
                settings,
                today,
                ct);
            var composedMeals = await ReplaceComposedMealsAsync(userId, today, ct);

            var foodLogs = BuildFoodLogs(userId, foodAndWeightFrom, today);
            _db.FoodLogs.AddRange(foodLogs);

            var weightLogsChanged = await UpsertWeightLogsAsync(
                userId,
                foodAndWeightFrom,
                today,
                ct);

            var sessions = BuildWorkoutSessions(
                userId,
                trainingFrom,
                exercises,
                plan);
            _db.WorkoutSessions.AddRange(sessions);

            await _db.SaveChangesAsync(ct);

            var report = await _weeklyReportService.RegenerateCurrentAsync(userId, ct);
            var developmentRecommendations = BuildDevelopmentRecommendations(
                userId,
                today,
                report,
                settings,
                exercises);
            _db.AdaptiveRecommendations.AddRange(developmentRecommendations);

            await _db.SaveChangesAsync(ct);

            return new DevelopmentSeedResult
            {
                UserId = userId,
                FoodLogs = foodLogs.Count,
                WeightLogs = weightLogsChanged,
                WorkoutSessions = sessions.Count,
                Exercises = exercises.Count,
                WorkoutPrograms = 1,
                Workouts = plan.Workouts.Count,
                ComposedMeals = composedMeals,
                ExerciseTargets = exerciseTargets,
                NutritionTargetsHistory = nutritionTargetsHistory,
                AdaptiveRecommendations =
                    report.Recommendations.Count + developmentRecommendations.Count,
                FoodAndWeightFromUtc = foodAndWeightFrom,
                FoodAndWeightToUtc = today,
                TrainingFromUtc = trainingFrom,
                TrainingToUtc = sessions.Max(x => x.StartedAtUtc).Date
            };
        }

        private async Task RemoveExistingSeedDataAsync(
            string userId,
            CancellationToken ct)
        {
            var seedNutritionTargets = await _db.NutritionTargetsHistory
                .Where(x => x.UserId == userId && x.Source == SeedSourceType)
                .ToListAsync(ct);

            if (seedNutritionTargets.Count > 0)
            {
                _db.NutritionTargetsHistory.RemoveRange(seedNutritionTargets);
            }

            var seedReports = await _db.WeeklyReports
                .Where(x =>
                    x.UserId == userId &&
                    x.AlgorithmVersion == WeeklyReportService.AlgorithmVersion)
                .ToListAsync(ct);

            if (seedReports.Count > 0)
            {
                _db.WeeklyReports.RemoveRange(seedReports);
            }

            var seedFoodLogs = await _db.FoodLogs
                .Where(x => x.UserId == userId && x.SourceType == SeedSourceType)
                .ToListAsync(ct);

            if (seedFoodLogs.Count > 0)
            {
                _db.FoodLogs.RemoveRange(seedFoodLogs);
            }

            var seedSessions = await _db.WorkoutSessions
                .Where(x =>
                    x.UserId == userId &&
                    x.ClientRequestId != null &&
                    x.ClientRequestId.StartsWith(SeedSessionPrefix))
                .ToListAsync(ct);

            if (seedSessions.Count > 0)
            {
                _db.WorkoutSessions.RemoveRange(seedSessions);
            }

            var seedComposedMeals = await _db.ComposedMeals
                .Where(x => x.UserId == userId && x.Name.StartsWith("Mock "))
                .ToListAsync(ct);

            if (seedComposedMeals.Count > 0)
            {
                _db.ComposedMeals.RemoveRange(seedComposedMeals);
            }

            var seedWorkouts = await _db.Workouts
                .Where(x => x.UserId == userId && x.Name.StartsWith("Mock "))
                .ToListAsync(ct);

            if (seedWorkouts.Count > 0)
            {
                _db.Workouts.RemoveRange(seedWorkouts);
            }

            var seedPrograms = await _db.WorkoutPrograms
                .Where(x => x.UserId == userId && x.Name == SeedProgramName)
                .ToListAsync(ct);

            if (seedPrograms.Count > 0)
            {
                _db.WorkoutPrograms.RemoveRange(seedPrograms);
            }

            if (seedNutritionTargets.Count > 0 ||
                seedReports.Count > 0 ||
                seedFoodLogs.Count > 0 ||
                seedSessions.Count > 0 ||
                seedComposedMeals.Count > 0 ||
                seedWorkouts.Count > 0 ||
                seedPrograms.Count > 0)
            {
                await _db.SaveChangesAsync(ct);
            }
        }

