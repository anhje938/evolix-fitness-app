export type TranslationKey = keyof typeof translations.nb;

export function localeForLanguage(language: AppLanguage) {
  return language === "en" ? "en-US" : "nb-NO";
}

export function translate(
  language: AppLanguage,
  key: TranslationKey,
  params?: TranslationParams
) {
  let value: string = translations[language]?.[key] ?? translations.nb[key] ?? key;

  if (params) {
    for (const [paramKey, paramValue] of Object.entries(params)) {
      value = value.split(`{${paramKey}}`).join(String(paramValue));
    }
  }

  return value;
}

export function useTranslation() {
  const { userSettings } = useUserSettings();
  const language = userSettings.language ?? "nb";

  const t = useCallback(
    (key: TranslationKey, params?: TranslationParams) =>
      translate(language, key, params),
    [language]
  );

  return useMemo(
    () => ({
      language,
      locale: localeForLanguage(language),
      t,
    }),
    [language, t]
  );
}
